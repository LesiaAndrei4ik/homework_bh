'''
Запросить 3 раза строку из нескольких чисел через пробел
    - вывести все уникальные числа по возрастанию
    - вывести числа которые есть в каждой строке
    -* вывести числа которые есть только в одной из трех строк
    
    выполнить без циклов и условий
    
    пример:
    >>> 1 2 11 22
    >>> 1 2 22 33
    >>> 1 2 33 44


    1) 1 2 11 22 33 44
    2) 1 2
    3) 11 44
    
'''

# вводим три строки чисел
s1 = input("Введите числа первой строки через пробел: ")
s2 = input("Введите числа второй строки через пробел: ")
s3 = input("Введите числа третьей строки через пробел: ")

# превращаем строки в множества чисел
set1 = set(map(int, s1.split()))
set2 = set(map(int, s2.split()))
set3 = set(map(int, s3.split()))

# 1) все уникальные числа по возрастанию. | - объединение множеств. sorted() - сортирует результат по возрастанию.
all_unique = sorted(set1 | set2 | set3)
print("Все уникальные числа по возрастанию:", all_unique)

# 2) числа, которые есть в каждой строке. & - пересечение множеств: числа, которые есть во всех трёх множествах.
common = sorted(set1 & set2 & set3)
print("Числа, которые есть в каждой строке:", common)

