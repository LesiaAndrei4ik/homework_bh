"""
Запросить у пользователя год рождения и в соответствии с его возрастом 
охарактеризовать пользователя - 
ребенок, подросток, юноша, в расцвете сил, пожилой, старик.
"""

# запрашиваем год рождения
year = int(input("Введите ваш год рождения: "))

# вычисляем возраст (предположим, текущий год 2025)
age = 2025 - year

# определяем характеристику
if age <= 12:
    print("Вы ребёнок")
elif age <= 17:
    print("Вы подросток")
elif age <= 25:
    print("Вы юноша")
elif age <= 45:
    print("Вы в расцвете сил")
elif age <= 65:
    print("Вы пожилой")
else:
    print("Вы старик")


'''
year = int(input(...)) - пользователь вводит год рождения, превращаем в число.
age = 2025 - year - считаем возраст.
if ... elif ... else ... — проверяем диапазон возраста и выводим описани
'''