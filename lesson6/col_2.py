"""
2. Создать структуру данных сотрудников фирмы с 
    тремя сотрудниками. каждый сотрудник должен иметь:
        ФИО, 
        должность, 
        год рождения, 
        список навыков, 
        список детей с их именем и годом рождения. 
    
    Запросить ФИО сотрудника и вывести по нему информацию.
    
    ** задать вопрос о желании добавить сотрудника,
        если ответ да - добавить сотрудника через несколько input
        (после добавления сотрудника вывести всю структуру консоль)

"""


# создаём структуру сотрудников

employees = {
    "Иванов Иван Иванович": {
        "должность": "Менеджер",
        "год рождения": 1985,
        "навыки": ["Excel", "Продажи"],
        "дети": [
            {"имя": "Аня", "год": 2010},
            {"имя": "Петя", "год": 2015}
        ]
    },
    "Петров Пётр Петрович": {
        "должность": "Бухгалтер",
        "год рождения": 1990,
        "навыки": ["1С", "Касса"],
        "дети": []
    },
    "Сидоров Сидор Сидорович": {
        "должность": "Разработчик",
        "год рождения": 1995,
        "навыки": ["Python", "Git", "SQL"],
        "дети": [
            {"имя": "Маша", "год": 2020}
        ]
    }
}

# ищем сотрудника
find_name = input("Введите ФИО сотрудника: ")

if find_name in employees:
    emp = employees[find_name]      # сокаращем для удобства
    print("Должность:", emp["должность"])
    print("Год рождения:", emp["год рождения"])
    print("Навыки:", emp["навыки"])
    print("Дети:", emp["дети"])
else:
    print("Такого сотрудника нет")

# спрашиваем — добавить ли сотрудника
answer = input("Хотите добавить сотрудника? (да/нет): ")

if answer == "да":
    fio = input("Введите ФИО: ")
    position = input("Введите должность: ")
    year = int(input("Введите год рождения: "))

    # навыки
    skills = []
    s1 = input("Введите первый навык: ")
    skills.append(s1)           # добавляем скилл1
    s2 = input("Введите второй навык: ")
    skills.append(s2)

    # дети
    children = []
    add_child = input("Есть дети? (да/нет): ")
    if add_child == "да":
        name_child = input("Имя ребёнка: ")
        year_child = int(input("Год рождения ребёнка: "))
        children.append({"имя": name_child, "год": year_child})

    # сохраняем сотрудника
    employees[fio] = {
        "должность": position,
        "год рождения": year,
        "навыки": skills,
        "дети": children
    }

    print("\nСтруктура сотрудников теперь такая:")
    print(employees)