"""
Даны 4 переменные - a1, a2, a3, a4.
1 - вывести True если все они дробные числа
2 - вывести True если одна из них строка
3 - вывести True если  одна пара переменных является целочисленным типом. 
    Пары могут образовать только следующие переменные - a1-a3, a2-a4, a3-a4"
"""



# пример значений
a1 = 1.5
a2 = 2.0
a3 = 3.0
a4 = 4

# 1) все дробные числа
all_float = all(isinstance(x, float) for x in (a1, a2, a3, a4))
print("Все дробные числа:", all_float)

# 2) хотя бы одна строка
one_string = any(isinstance(x, str) for x in (a1, a2, a3, a4))
print("Хотя бы одна строка:", one_string)

# 3) хотя бы одна допустимая пара целых чисел
pairs = [(a1, a3), (a2, a4), (a3, a4)]
pair_int = any(all(isinstance(x, int) for x in pair) for pair in pairs)
print("Хотя бы одна допустимая пара целых чисел:", pair_int)



"""
1. (a1, a2, a3, a4) - создаём кортеж из четырёх переменных.
isinstance(x, float) - проверяет, является ли переменная x дробным числом (float).
for x in (a1, a2, a3, a4) - последовательно проверяем каждую переменную.
all(...) - возвращает True, если все проверки вернули True.
Результат all_float = False, потому что не все переменные дробные.

2. isinstance(x, str) - проверяет, является ли переменная строкой.
any(...) - возвращает True, если хотя бы одна проверка вернула True.
Пример: если a2 = "hello" → any(...) = True, иначе False.

3. Мы составили все допустимые пары переменных, которые нужно проверить:
a1–a3
a2–a4
a3–a4

all(isinstance(x, int) for x in pair)
Для каждой пары проверяем: оба элемента - целые числа (int).
all(...) вернёт True, только если оба числа в паре целые.
any(... for pair in pairs)
any(...) проверяет все пары.
Если хотя бы одна пара целых чисел → pair_int = True.

"""