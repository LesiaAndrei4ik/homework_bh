"""
Запросить любое число не менее 10. 
Вывести на экран сумму квадратов каждой цифры составляющей это число. 
Например: дано 236 => 2*2 + 3*3 + 6*6 = 49 

"""

# Запросим число
number = int(input("Введите число не менее 10: "))

# Проверим, что число действительно больше или равно 10
if number >= 10:
    # Зададим переменную для суммы квадратов
    sum_of_squares = 0

    # Преобразуем число в строку, чтобы пройти по каждой цифре
    for digit in str(number):
        # Преобразуем символ в цифру, возводим в квадрат и добавляем к сумме
        sum_of_squares += int(digit) ** 2

    # Выводим результат
    print("Сумма квадратов цифр числа:", sum_of_squares)
else:
    print("Число должно быть не менее 10.")


    
    
    
"""
Цикл - for digit in str(number): — он перебирает каждый символ строки, полученной из числа. В данном случае, 
строка — это каждая цифра числа в виде символа.
digit — переменная, которая на каждой итерации цикла будет содержать поочередно одну цифру числа, но как строку. Наприм, если
чмсло 235, цикл выполнится 3 раза: В первой итерации digit будет '2', во второй digit будет '3', в третьей будет '6'.

int(digit) ** 2 — преобразуем строку digit обратно в число с помощью int(digit), возводим её в квадрат и добавляем к сумме.
"""