"""
Запросить у учителя оценки ученика по одной до тех пор пока он не введет 0. 
Выдать средний бал ученика.

"""

total = 0
count = 0

print("Введите оценки ученика (0 — завершение ввода):")

# Цикл для ввода оценок
while True:
    try:
        grade = int(input("Оценка: "))  # Просим пользователя ввести оценку
        if grade == 0:
            break                       # выход из цикла при вводе 0                           
        if 1 <= grade <= 5:             # проверка на корректность оценки (1–5)       
            total += grade
            count += 1
        else:
            print("Ошибка: оценка должна быть от 1 до 5. Попробуйте ещё раз.")
    except ValueError:
        print("Ошибка: введите целое число. Попробуйте ещё раз.")

# Вычисление и вывод среднего балла
if count > 0:
    average = total / count
    print(f"Средний балл: {average:.2f}")
else:
    print("Оценки не были введены.")