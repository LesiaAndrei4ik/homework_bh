'''
* - елочка со снегом
'''

import random                              

height = int(input("Введите высоту ёлочки (от 3 до 20): "))     

# Проверяем корректность ввода в пределах заданных чисел
if 3 <= height <= 20:

    # Начинаем цикл по строчкам ёлочки
    for i in range(height):

        # левая часть строки (где будут пробелы или снег)
        left_width = height - i - 1
        # ширина ёлки
        tree_width = 2 * i + 1
        # правая часть строки
        right_width = left_width

        # Генерим левый снег
        left = ""
        for _ in range(left_width):
            left += random.choice([" ", ".", "*", "❄", " "])  # пробелов больше

        # Генерим саму ёлку
        tree = "*" * tree_width

        # Генерируем правый снег
        right = ""
        for _ in range(right_width):
            right += random.choice([" ", ".", "*", "❄", " "])

        # Печатаем строку
        print(left + tree + right)

else:
    print("Высота должна быть от 3 до 20.")