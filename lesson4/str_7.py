'''
Запросить число
вывести это число разбитое на порядки
    Пример:
    1234567 -> 1 234 567
    5678 -> 5 678
    
    (решить без for)
    
'''

num = int(input("Введите число: "))
print(f"{num:,}".replace(",", " "))


'''
int(input()) — запрашиваем число и преобразуем в целое
f"{num:,}" — форматирует число с разделителем тысяч через запятую (например, 1,234,567).
.replace(",", " ") — заменяем запятые на пробелы, как в примере.
'''



